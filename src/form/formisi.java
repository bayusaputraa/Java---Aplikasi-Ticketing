    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here: Simpan
        try {
            st = cn.createStatement();
            if (txtNama.getText().equals("") || txtTlp.getText().equals("")|| txtNo.getText().equals("")|| txtTgl.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            //aksi simpan data
            if (btnSimpan.getText() == "Simpan") {
                String cek = "SELECT * FROM  data WHERE No ='" + txtNo.getText() + "'";
                rs =st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null,"Upps.. Data ini sudah ada");
                }else{
                    String sql = "INSERT INTO data VALUES('" + txtNama.getText() +"', '" + txtTlp.getText() +"', '" + txtNo.getText() +"', '" + txtTgl.getText() +"', '" + cmbWahana.getSelectedItem().toString() +"')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Data berhasil dimasukan");
                    Bersih();
                    TampilData();
                }
            }else{
                //aksi ubah data
                String update = "UPDATE `data` SET `Nama`='" + txtNama.getText() + "', `Tlp`='" + txtTlp.getText() + "', `Wahana`='" + txtWahana.getText() + "' WHERE `No`='" + txtNo.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                Bersih();
                TampilData();
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

