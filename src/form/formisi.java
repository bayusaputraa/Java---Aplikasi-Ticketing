    private void CariData(){
        //Class untuk mencari data yang berdasarkan pilihan dicombo box (Nama dan Nomor)
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM data WHERE " + cmbCari.getSelectedItem().toString() + " LIKE '%" + txtCari.getText() + "%'");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nama");
            model.addColumn("Tlp");
            model.addColumn("No");
            model.addColumn("Tgl");
            model.addColumn("Wahana");
            
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data ={
                    rs.getString("Nama"),
                    rs.getString("Tlp"),
                    rs.getString("No"),
                    rs.getString("Tgl"),
                    rs.getString("Wahana")
                };
               model.addRow(data);
               tblData.setModel(model);
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    
    }
    
